    private void hienthisach() throws SQLException{
        SachController sachController = new SachController();
        List<SachModel> list = sachController.getAllSach();
        DefaultTableModel model = (DefaultTableModel) tblQuanlysach.getModel();
        // Xoa du lieu cu
        model.setRowCount(0);
        // Them du lieu moi
        for(SachModel sach : list){
            model.addRow(new Object[]{
                sach.getIdSach(),
                sach.getTenSach(),
                sach.getTenTacGia(),
                sach.getTenTheLoai(),
                sach.getSoLuong(),
                sach.getGiaSach(),
                sach.getNgayNhan(),
                sach.getNhaXuatBan(),
                sach.getMoTa()
            });
        }
    }
