    private void tblQuanlysachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblQuanlysachMouseClicked
        // TODO add your handling code here:
         int selectedRow = tblQuanlysach.getSelectedRow(); 
        if (selectedRow >= 0) {
            try {
                // Lấy model của bảng để truy xuất giá trị
                DefaultTableModel model = (DefaultTableModel) tblQuanlysach.getModel();

                // Lấy ID sách từ cột đầu tiên
                int idSach = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());

                // Lấy danh sách tất cả sách từ controller
                SachController sachCtrl = new SachController();
                List<SachModel> sachList = sachCtrl.getAllSach();

                // Tìm sách theo ID sách
                SachModel sach = null;
                for (SachModel s : sachList) {
                    if (s.getIdSach() == idSach) {
                        sach = s;
                        break;
                    }
                }

                if (sach != null) {
                    // Hiển thị thông tin vào các TextField
                    txtMasach.setText(String.valueOf(sach.getIdSach())); // ID sách
                    txtTenSach.setText(sach.getTenSach()); // Tên sách

                    // Hiển thị ID tác giả từ SachModel
                    txtIDTacgia.setText(String.valueOf(sach.getIdTacGia())); // ID tác giả từ SachModel

                    txtTenTacGia.setText(sach.getTenTacGia()); // Tên tác giả
                    txtSoLuong.setText(String.valueOf(sach.getSoLuong())); // Số lượng
                    txtGia.setText(String.valueOf(sach.getGiaSach())); // Giá sách
                    txtNgaynhap.setText(sach.getNgayNhan()); // Ngày nhận
                    txtNhaxuatban.setText(sach.getNhaXuatBan()); // Nhà xuất bản

                    // Lấy mô tả sách từ SachModel
                    txtAreaQLsach.setText(sach.getMoTa()); // Hiển thị mô tả vào TextArea

                    // Hiển thị thể loại trong ComboBox
                    cmbTheloai.setSelectedItem(sach.getTenTheLoai()); // Thể loại từ SachModel
                }
            } catch (SQLException ex) {
                Logger.getLogger(frmQLSach.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tblQuanlysachMouseClicked

